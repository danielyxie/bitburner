export function analyze(
  terminal: ITerminal,
  router: IRouter,
  player: IPlayer,
  server: BaseServer,
  args: (string | number)[],
): void {
  if (args.length !== 0) {
    terminal.print("Incorrect usage of analyze command. Usage: analyze");
    return;
  }
  terminal.startAnalyze();
}
